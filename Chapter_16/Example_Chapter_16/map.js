// object: {key, value} pair

// map: (key, value) pair

// new Map() 

const myMap = new Map(); // empty map
// add new data to use set() method 
myMap.set("name", "David"); // (key, value) set 
myMap.set("age", 25);
console.log(myMap);

// advantage of Map
/*
    1. any kind data(ex. string, number, object) can be stored in a Map as (key, value) pair 

    2. Get Map size using size() method 

    3. Get Property using get() method 
*/