// JavaScript variables are flexible 
// data types are number, string, boolean, array, object
// this data types included in variables 

let data = 43; // number
// flexible types 
// same variable can store different types of data 
data = "Hello, World!"; // string
data = true; // boolean
data = [10, 50, 66]; // array 
data = { name: 'cat', say: 'meow' };  // object
console.log(data);

// JavaScript is dynamic typing programming
// JavaScript variables types are dynamic
// also called loosely type programming language 
